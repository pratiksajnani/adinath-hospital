    init() {
        const saved = localStorage.getItem('hms_language');
        if (saved && ['en', 'hi', 'gu'].includes(saved)) {
            this.currentLanguage = saved;
        }
        this.applyTranslations();

        // Set up language buttons
        document.querySelectorAll('[data-lang]').forEach((btn) => {
            btn.addEventListener('click', () => {
                this.setLanguage(btn.dataset.lang);
            });
        });
    },

    // Set language
    setLanguage(lang) {
        if (!['en', 'hi', 'gu'].includes(lang)) {
            return;
        }
        this.currentLanguage = lang;
        localStorage.setItem('hms_language', lang);
        this.applyTranslations();
        document.documentElement.lang = lang;
    },

    // Get translation
    t(key, lang = null) {
        const translation = this.translations[key];
        if (!translation) {
            console.warn(`Missing translation: ${key}`);
            return key;
        }
        return translation[lang || this.currentLanguage] || translation['en'] || key;
    },

    // Apply translations to page
    applyTranslations() {
        document.querySelectorAll('[data-i18n]').forEach((el) => {
            const key = el.getAttribute('data-i18n');
            const translated = this.t(key);
            if (translated !== key) {
                el.textContent = translated;
            }
        });

        document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
            const key = el.getAttribute('data-i18n-placeholder');
            el.placeholder = this.t(key);
        });

        // Update language selector buttons
        document.querySelectorAll('[data-lang]').forEach((btn) => {
            btn.classList.toggle('active', btn.dataset.lang === this.currentLanguage);
        });
    },
